public class ComparisonOperator
{
	public static void main(String args[])
	{
		int v1=1;
		int v2=2;
		if(v1==v2)
		{
			System.out.println("Value1=Value2");
		}

		if(v1!=v2)
		{
			System.out.println("Value1!=Value2");
		}

		if(v1<=v2)
		{
			System.out.println("Value1<=Value2");
		}

		if(v1<v2)
		{
			System.out.println("Value1<Value2");
		}

		if(v1>=v2)
		{
			System.out.println("Value1>=Value2");
		}

		if(v1>v2)
		{
			System.out.println("Value1>Value2");
		}
	}
}